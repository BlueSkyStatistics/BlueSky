# arrow <- Vs double-arrow <<- Vs .GlobalEnv ------[inside any function]
#
# When we use double arrow <<- inside any function, R will search for the lefthand object searching all environments one by one.
# If the object is found in some package the object is locked and our call with <<- will fail
# If the object is not found then the object will be created outside the function scope.
#
# if we use single arrow <- then the object will be local to the function and is destroyed as soon as we leave the function.
#
# If we use .GlobalEnv$objname inside any function then R will only search for the object in the .GlobalEnv 
# if the object is found it wiil overwrite it.
# if the object is not found it will create it.
